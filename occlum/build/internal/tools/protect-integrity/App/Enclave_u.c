sgx_status_t ecall_protect(sgx_enclave_id_t eid, int* retval, const char* input_path, const char* output_path)
{
	sgx_status_t status;
	ms_ecall_protect_t ms;
	ms.ms_input_path = input_path;
	ms.ms_input_path_len = input_path ? strlen(input_path) + 1 : 0;
	ms.ms_output_path = output_path;
	ms.ms_output_path_len = output_path ? strlen(output_path) + 1 : 0;
	status = sgx_ecall(eid, 0, &ocall_table_Enclave, &ms);
	if (status == SGX_SUCCESS && retval) *retval = ms.ms_retval;
	return status;
}

